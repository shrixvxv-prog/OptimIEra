export function safeRouteError(error: unknown) {
  const requestId = randomUUID();
  const code =
    error instanceof Error && statusByCode[error.message] ? error.message : 'INTERNAL_ERROR';
  return Response.json(
    {
      error: {
        code,
        message: code === 'INTERNAL_ERROR' ? 'Something went wrong. Retry the request.' : code,
      },
      requestId,
    },
    { status: statusByCode[code] },
  );
}
